##HELSINGIN VENENÄYTTELYMATKA 10.2.2018

Matka Helsingin venenäyttelyyn tehdään lauantaina 10. helmikuuta. Lähtö klo 9.00 kaupungintalon edestä ja takaisin palaamme noin klo 20.00 samaan paikkaan. Bussin reitti on: Satakunnantie, Uudenmaankatu ja lopuksi Kaarinantietä pitkin Helsingin moottoritielle.  
Voit ottaa myös RVS:ään kuulumattomat kaverisi mukaan.
 
Ilmoittautumisen yhteydessä kerro mistä nouset kyytiin ja puhelinnumerosi.
Matkan hinta on 40,00 € / henkilö. (sis. matkalla kahvi + sämpylä + sisäänpääsymaksu)
